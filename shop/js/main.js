function applyCoupon() {
    const couponCode = document.getElementById('couponInput').value;
    const priceDisplay = document.getElementById('priceDisplay');
    const addToCartBtn = document.getElementById('addToCartBtn');

    if (couponCode === '2025') {
        countDown(699.99, 0, 1000);
        showFireworks();
        setTimeout(() => {
            priceDisplay.classList.add('blink-text');
            addToCartBtn.classList.add('blink-button');
            addToCartBtn.classList.remove('text-primary');
        }, 1000);
    }
}

function countDown(start, end, duration) {
    const priceDisplay = document.getElementById('priceDisplay');
    const startTime = performance.now();
    
    function updatePrice(currentTime) {
        const elapsedTime = currentTime - startTime;
        if (elapsedTime < duration) {
            const remainingTime = duration - elapsedTime;
            const currentPrice = start - (start - end) * (1 - remainingTime / duration);
            priceDisplay.textContent = '$' + currentPrice.toFixed(2);
            requestAnimationFrame(updatePrice);
        } else {
            priceDisplay.textContent = '$' + end.toFixed(2);
        }
    }
    
    requestAnimationFrame(updatePrice);
}

function showFireworks() {
    const fireworks = document.querySelector('.fireworks');
    fireworks.hidden = false;
    
    setTimeout(() => {
        fireworks.hidden = true;
    }, 10000); // Hide after 10 seconds
}
